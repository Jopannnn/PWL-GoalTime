    <h1 style="font-size: 70px; margin-top: 120px; margin-left: 250px;">Tentang Kami</h1>

    <div class="kotak-penjelasan">
    <p style="font-size: 30px;" class="dalam-kotak-penjelasan">Goaltime adalah penyedia layanan revisi dan perawatan lapangan futsal yang berdedikasi menghadirkan kualitas terbaik bagi para pecinta olahraga. Kami memahami bahwa lapangan yang nyaman dan terawat adalah kunci utama dalam menciptakan pengalaman bermain yang seru dan aman. Oleh karena itu, Goaltime hadir dengan layanan menyeluruh, mulai dari perbaikan dan penggantian rumput sintetis, pengecekan serta peningkatan sistem pencahayaan, perawatan garis lapangan, hingga penataan fasilitas pendukung lainnya. Dengan tim yang berpengalaman dan menggunakan material berkualitas tinggi, kami berkomitmen menjaga standar profesional dalam setiap pekerjaan yang kami lakukan. Tidak hanya sebatas memperbaiki, Goaltime juga berusaha menciptakan suasana lapangan yang mampu meningkatkan semangat dan performa para pemain. Bagi kami, setiap detail sangat penting karena futsal bukan sekadar permainan, melainkan momen kebersamaan, sportivitas, dan kegembiraan. Dengan Goaltime, wujudkan lapangan futsal impian Anda yang aman, nyaman, dan siap menjadi arena penuh aksi.</p>
    </div>

    <h1 style="font-size: 70px; margin-top: 120px; margin-left: 250px;">Cerita GoalTime</h1>

    <div class="kotak-penjelasan">
    <p style="font-size: 30px;" class="dalam-kotak-penjelasan">Goaltime hadir dari sebuah mimpi sederhana yang lahir di tengah lapangan futsal. Berawal dari pengalaman pribadi para pendiri yang sering kecewa karena kondisi lapangan yang tidak terawat—rumput sintetis yang mulai sobek, lantai yang licin, lampu yang padam di tengah permainan, hingga garis lapangan yang pudar—kami merasa futsal kehilangan keseruannya. Padahal, futsal bukan sekadar olahraga; ia adalah wadah untuk melepaskan penat, menjalin persahabatan, membangun kekompakan, dan menyalurkan semangat kompetisi sehat. Dari situlah muncul tekad untuk menghadirkan solusi yang nyata. Goaltime pun berdiri sebagai layanan yang berfokus pada revisi dan perawatan lapangan futsal dengan standar terbaik, karena kami percaya bahwa pengalaman bermain yang maksimal berawal dari kondisi lapangan yang ideal.</p>    
    </div>

    <div class="kotak-penjelasan">
    <p style="font-size: 30px;" class="dalam-kotak-penjelasan">Kini, Goaltime tumbuh menjadi mitra terpercaya bagi para pemilik lapangan futsal di berbagai daerah. Dengan dukungan tim profesional, penggunaan material pilihan, dan pendekatan kerja yang detail, kami menangani berbagai kebutuhan—mulai dari pemasangan dan perbaikan rumput sintetis, perawatan pencahayaan agar permainan tetap optimal meski malam hari, hingga peningkatan fasilitas pendukung lain yang membuat pemain semakin betah. Lebih dari sekadar jasa perawatan, Goaltime adalah wujud kepedulian terhadap dunia futsal dan semangat olahraga di tanah air. Kami ingin setiap lapangan yang kami tangani menjadi arena penuh energi, tempat di mana tawa, keringat, semangat, dan kebersamaan berpadu menjadi satu. Dengan Goaltime, kami percaya bahwa setiap permainan akan selalu berakhir dengan pengalaman tak terlupakan, dan setiap momen di lapangan akan menjadi cerita yang pantas dikenang.</p>    
    </div>




